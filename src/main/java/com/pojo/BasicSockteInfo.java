package com.pojo;

public class BasicSockteInfo {
  private String configIP;
  private String port;
  private String CustomBroadcast;

    public String getConfigIP() {
        return configIP;
    }

    public void setConfigIP(String configIP) {
        this.configIP = configIP;
    }

    public String getPort() {
        return port;
    }

    public void setPort(String port) {
        this.port = port;
    }

    public String getCustomBroadcast() {
        return CustomBroadcast;
    }

    public void setCustomBroadcast(String customBroadcast) {
        CustomBroadcast = customBroadcast;
    }
}
