package com.pojo;


public class BasicStaff {

  private long staffId;
  private String staffCode;
  private String staffName;
  private String staffTel;
  private String staffTitle;
  private String staffLoginName;
  private String staffPassword;
  private String staffPhotoAddress;
  private String staffNote;
  private long staffStatus;
  private long staffType;
  private long districtId;


  public long getStaffId() {
    return staffId;
  }

  public void setStaffId(long staffId) {
    this.staffId = staffId;
  }


  public String getStaffCode() {
    return staffCode;
  }

  public void setStaffCode(String staffCode) {
    this.staffCode = staffCode;
  }


  public String getStaffName() {
    return staffName;
  }

  public void setStaffName(String staffName) {
    this.staffName = staffName;
  }


  public String getStaffTel() {
    return staffTel;
  }

  public void setStaffTel(String staffTel) {
    this.staffTel = staffTel;
  }


  public String getStaffTitle() {
    return staffTitle;
  }

  public void setStaffTitle(String staffTitle) {
    this.staffTitle = staffTitle;
  }


  public String getStaffLoginName() {
    return staffLoginName;
  }

  public void setStaffLoginName(String staffLoginName) {
    this.staffLoginName = staffLoginName;
  }


  public String getStaffPassword() {
    return staffPassword;
  }

  public void setStaffPassword(String staffPassword) {
    this.staffPassword = staffPassword;
  }


  public String getStaffPhotoAddress() {
    return staffPhotoAddress;
  }

  public void setStaffPhotoAddress(String staffPhotoAddress) {
    this.staffPhotoAddress = staffPhotoAddress;
  }


  public String getStaffNote() {
    return staffNote;
  }

  public void setStaffNote(String staffNote) {
    this.staffNote = staffNote;
  }


  public long getStaffStatus() {
    return staffStatus;
  }

  public void setStaffStatus(long staffStatus) {
    this.staffStatus = staffStatus;
  }


  public long getStaffType() {
    return staffType;
  }

  public void setStaffType(long staffType) {
    this.staffType = staffType;
  }


  public long getDistrictId() {
    return districtId;
  }

  public void setDistrictId(long districtId) {
    this.districtId = districtId;
  }

}
